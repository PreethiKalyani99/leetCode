function repeatedSubArrayLength(nums1, nums2){
    let result = 0

    for(let i = 0; i < (nums1.length + nums2.length - 1); i++){
        let n1 = Math.max(0, (nums1.length - 1 - i))
        let n2 = Math.max(0, i - (nums2.length - 1))
        let matchSequence = 0
        for(let num1 = n1, num2 = n2; num1 < nums1.length && num2 < nums2.length; ++num1, ++num2){
            if(nums1[num1] === nums2[num2]){
                matchSequence += 1
            }
            else{
                matchSequence = 0
            }
            result = Math.max(result, matchSequence)
        }
    }
    return result
}
